document.addEventListener("DOMContentLoaded", () => {
    const container = document.getElementById("lyricsContainer");
    const searchInput = document.getElementById("searchInput");
    const navButtons = document.querySelectorAll(".nav-btn");
    const scrollTopBtn = document.getElementById("scrollTopBtn");

    let currentCategory = "all";
    let searchQuery = "";

    function renderCards() {
        container.innerHTML = "";

        const filteredData = cheerData.filter(item => {
            const matchesCategory = (currentCategory === "all" || item.category === currentCategory);
            const matchesSearch = item.name.includes(searchQuery) || item.number.includes(searchQuery);
            return matchesCategory && matchesSearch;
        });

        if (filteredData.length === 0) {
            container.innerHTML = '<div class="no-result">該当する応援歌が見つかりません</div>';
            return;
        }

        filteredData.forEach(item => {
            const card = document.createElement("div");
            
            // ★修正：ポジションタイプごとのテーマクラス（~-theme）を動的に追加
            card.className = `lyrics-card ${item.category}-theme`;
            
            const hasImage = item.image && item.image.trim() !== "";
            const imageHtml = hasImage 
                ? `<div class="player-thumb"><img src="${item.image}" alt="${item.name}"></div>` 
                : "";

            card.innerHTML = `
                <button class="card-toggle-trigger" aria-expanded="false">
                    ${imageHtml}
                    <div class="card-header-text">
                        <span class="number">${item.number}</span>
                        <h2 class="name">${item.name}</h2>
                    </div>
                    <span class="arrow-icon">▼</span>
                </button>
                <div class="card-body-wrapper">
                    <div class="card-body">
                        ${item.lyrics}
                    </div>
                </div>
            `;

            const triggerBtn = card.querySelector(".card-toggle-trigger");
            triggerBtn.addEventListener("click", () => {
                const isOpen = card.classList.toggle("open");
                triggerBtn.setAttribute("aria-expanded", isOpen);
            });

            container.appendChild(card);
        });
    }

    searchInput.addEventListener("input", (e) => {
        searchQuery = e.target.value.trim();
        renderCards();
    });

    navButtons.forEach(btn => {
        btn.addEventListener("click", (e) => {
            navButtons.forEach(b => b.classList.remove("active"));
            e.target.classList.add("active");
            currentCategory = e.target.getAttribute("data-target");
            renderCards();
        });
    });

    window.addEventListener("scroll", () => {
        if (window.scrollY > 300) {
            scrollTopBtn.classList.add("show");
        } else {
            scrollTopBtn.classList.remove("show");
        }
    });

    scrollTopBtn.addEventListener("click", () => {
        window.scrollTo({ top: 0, behavior: "smooth" });
    });

    renderCards();
});